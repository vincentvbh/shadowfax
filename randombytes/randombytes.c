
#include "rng.h"
#include "sys_rand.h"
#include "randombytes.h"

prng p;

const
uint8_t _seed[48] = {
    0xf, 0xa, 0x3, 0x4, 0xb, 0xc, 0xe, 0x1, 0x5, 0x9, 0x8, 0x7, 0xd, 0x0, 0x6, 0x2,
    0xd, 0x1, 0x9, 0x7, 0xa, 0x5, 0x2, 0x0, 0xc, 0x8, 0xb, 0x4, 0x3, 0xe, 0x6, 0xf,
    0x9, 0xc, 0x8, 0xb, 0xf, 0xa, 0x1, 0xd, 0x3, 0x4, 0x7, 0x6, 0xe, 0x5, 0x0, 0x2
};

int randombytes(uint8_t *buf, size_t n){
    return prng_get_bytes(&p, buf, (int)n);
}

void randombytes_init(uint8_t *seed, size_t seed_len){
    prng_init(&p, seed, seed_len, 0);
}

uint64_t get64(){
    return prng_get_u64(&p);
}

uint8_t get8(){
    return prng_get_u8(&p);
}

void seed_rng(void){
    // must not exceed 48 bytes
    uint8_t seed[48];

    get_seed(seed, sizeof seed);

    prng_init(&p, seed, sizeof seed, 0);
}

void init_prng(void){
    prng_init(&p, _seed, sizeof _seed, 0);
}

